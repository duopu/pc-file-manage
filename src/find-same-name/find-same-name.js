    /**
     * ÈÄíÂΩíÊâ´ÊèèÁõÆÂΩï
     * @param {string} dirPath - ÁõÆÂΩïË∑ØÂæÑ
     */
    async scanDirectory(dirPath) {
        try {
            const items = await fs.promises.readdir(dirPath);

            for (const item of items) {
                const fullPath = path.join(dirPath, item);

                try {
                    const stat = await fs.promises.stat(fullPath);

                    if (stat.isDirectory()) {
                        this.stats.totalDirectories++;
                        await this.scanDirectory(fullPath); // ÈÄíÂΩíÊâ´ÊèèÂ≠êÁõÆÂΩï
                    } else if (stat.isFile()) {
                        this.stats.totalFiles++;
                        await this.processFile(fullPath, stat);
                    }
                } catch (error) {
                    console.warn(`Êó†Ê≥ïËÆøÈóÆ: ${fullPath} - ${error.message}`);
                }
            }
        } catch (error) {
            console.error(`Êâ´ÊèèÁõÆÂΩïÂ§±Ë¥•: ${dirPath} - ${error.message}`);
        }
    }

    /**
     * Â§ÑÁêÜÂçï‰∏™Êñá‰ª∂
     * @param {string} filePath - Êñá‰ª∂Ë∑ØÂæÑ
     * @param {fs.Stats} stat - Êñá‰ª∂ÁªüËÆ°‰ø°ÊÅØ
     */
    async processFile(filePath, stat) {
        const fileName = path.basename(filePath);
        const fileExt = path.extname(filePath).toLowerCase();
        const fileSize = stat.size;
        const modifiedTime = stat.mtime;

        // Ëé∑ÂèñÊñá‰ª∂ÁöÑMD5ÂìàÂ∏åÂÄºÔºàÁî®‰∫éÂå∫ÂàÜÂÜÖÂÆπÁõ∏ÂêåÁöÑÊñá‰ª∂Ôºâ
        let fileHash = '';
        try {
            const fileBuffer = await fs.promises.readFile(filePath);
            fileHash = crypto.createHash('md5').update(fileBuffer).digest('hex');
        } catch (error) {
            console.warn(`Êó†Ê≥ïËØªÂèñÊñá‰ª∂ÂìàÂ∏å: ${filePath}`);
        }

        const fileInfo = {
            name: fileName,
            path: filePath,
            directory: path.dirname(filePath),
            extension: fileExt,
            size: fileSize,
            sizeFormatted: this.formatFileSize(fileSize),
            modifiedTime: modifiedTime,
            modifiedTimeFormatted: modifiedTime.toLocaleString('zh-CN'),
            hash: fileHash
        };

        // Ê£ÄÊü•ÊòØÂê¶Â∑≤Â≠òÂú®ÂêåÂêçÊñá‰ª∂
        if (this.fileMap.has(fileName)) {
            // Â¶ÇÊûúÂ∑≤Â≠òÂú®ÔºåÊ∑ªÂä†Âà∞ÈáçÂ§çÊñá‰ª∂ÂàóË°®
            if (!this.duplicates.has(fileName)) {
                this.duplicates.set(fileName, [this.fileMap.get(fileName)]);
            }
            this.duplicates.get(fileName).push(fileInfo);
        } else {
            // È¶ñÊ¨°ÈÅáÂà∞ËØ•Êñá‰ª∂Âêç
            this.fileMap.set(fileName, fileInfo);
        }
    }

    /**
     * Ê†ºÂºèÂåñÊñá‰ª∂Â§ßÂ∞è
     * @param {number} bytes - Â≠óËäÇÊï∞
     * @returns {string} Ê†ºÂºèÂåñÂêéÁöÑÊñá‰ª∂Â§ßÂ∞è
     */
    formatFileSize(bytes) {
        if (bytes === 0) return '0 B';
        const k = 1024;
        const sizes = ['B', 'KB', 'MB', 'GB', 'TB'];
        const i = Math.floor(Math.log(bytes) / Math.log(k));
        return parseFloat((bytes / Math.pow(k, i)).toFixed(2)) + ' ' + sizes[i];
    }

